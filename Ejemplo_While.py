numero = 101        #Creamos la variable que cambiará a través del While
while numero > 0:   #Creamos la estructura While que funcionará hasta que numero sea menor que 0
    numero -= 1     #Restamos 1 a la variable numero
    print(numero)   #Imprimimos la variable numero
